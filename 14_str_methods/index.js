// String methods
let name = "Abhi";

// 1. length (no parentheses!)
let len = name.length;
console.log(len); // Output: 4

// 2. Uppercase (capital "U" and capital "C" in toUpperCase)
let up = name.toUpperCase();
console.log(up); // Output: ABHI

// 3. Lowercase (capital "L" and capital "C" in toLowerCase)
console.log(name.toLowerCase()); // Output: abhi

// 4. Slice (example: take substring from index 1 to 3)
let sliced = name.slice(1, 3);
console.log(sliced); // Output: "bh"
// reverse
let rv = name.split("").reverse().join("");;
console.log(rv);

